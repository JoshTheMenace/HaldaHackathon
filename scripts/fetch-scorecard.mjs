// Pre-fetch real College Scorecard data for well-known schools NOT in our seeded
// catalog (MIT, Stanford, Ivies, big publics, transfer-friendly CCs…) and cache
// it to lib/scorecard.data.ts — same pattern as fetch-rmp.mjs. The app reads the
// cache first and only hits the live API for the long tail.
//   node --env-file=.env.local scripts/fetch-scorecard.mjs
import { writeFileSync } from "node:fs";

const KEY = process.env.SCORECARD_API_KEY;
if (!KEY) { console.error("Set SCORECARD_API_KEY (e.g. node --env-file=.env.local ...)"); process.exit(1); }

const FIELDS = [
  "school.name", "school.city", "school.state",
  "latest.admissions.admission_rate.overall",
  "latest.cost.avg_net_price.overall",
  "latest.student.size",
  "latest.completion.rate_suppressed.overall",
  "latest.earnings.10_yrs_after_entry.median",
].join(",");

// Schools likely asked about that AREN'T in the seeded 17 — the reach schools,
// big names, and transfer-friendly community colleges the personas target.
const NAMES = [
  "Massachusetts Institute of Technology", "Stanford University", "California Institute of Technology",
  "Harvard University", "Princeton University", "Yale University", "Columbia University in the City of New York",
  "University of Pennsylvania", "Cornell University", "Brown University", "Dartmouth College",
  "University of California-Berkeley", "University of California-Los Angeles", "University of California-San Diego",
  "University of California-Irvine", "University of Southern California", "New York University",
  "University of Michigan-Ann Arbor", "University of North Carolina at Chapel Hill", "University of Virginia",
  "Carnegie Mellon University", "Johns Hopkins University", "Duke University", "Northwestern University",
  "University of Illinois Urbana-Champaign", "University of Wisconsin-Madison", "Boston University",
  "Arizona State University Campus Immersion", "University of Florida", "Ohio State University-Main Campus",
  "Salt Lake Community College", "Santa Monica College", "Houston Community College", "Austin Community College District",
];

const norm = (s) => s.toLowerCase().replace(/[^a-z0-9 ]/g, " ").replace(/\s+/g, " ").trim();
const tokens = (s) => new Set(norm(s).split(" ").filter((t) => t.length > 2 && !["the", "of", "at", "in"].includes(t)));

// The name search is fuzzy and returns many partial hits — pick the result whose
// name actually matches the query (exact normalized, else best token overlap).
function bestMatch(results, query) {
  const q = norm(query), qt = tokens(query);
  const named = results.filter((r) => r["school.name"]);
  // 1) exact normalized name
  for (const r of named) if (norm(r["school.name"]) === q) return r;
  // 2) result name starts with the query (e.g. "University of Virginia-Main Campus")
  const starts = named.filter((r) => norm(r["school.name"]).startsWith(q));
  if (starts.length) return starts.sort((a, b) => norm(a["school.name"]).length - norm(b["school.name"]).length)[0];
  // 3) all query tokens present, fewest extras
  let best = null, bestScore = -1;
  for (const r of named) {
    const rt = tokens(r["school.name"]);
    const recall = [...qt].filter((t) => rt.has(t)).length / Math.max(1, qt.size);
    if (recall < 1) continue;
    const score = 1 - [...rt].filter((t) => !qt.has(t)).length * 0.08;
    if (score > bestScore) { bestScore = score; best = r; }
  }
  return best;
}

function mapResult(r) {
  const num = (v) => (typeof v === "number" ? v : undefined);
  return {
    name: r["school.name"],
    city: r["school.city"] || undefined,
    state: r["school.state"] || undefined,
    acceptanceRate: num(r["latest.admissions.admission_rate.overall"]),
    netPrice: num(r["latest.cost.avg_net_price.overall"]),
    size: num(r["latest.student.size"]),
    completionRate: num(r["latest.completion.rate_suppressed.overall"]),
    medianEarnings: num(r["latest.earnings.10_yrs_after_entry.median"]),
  };
}

const out = [];
for (const name of NAMES) {
  const url = `https://api.data.gov/ed/collegescorecard/v1/schools?api_key=${KEY}&school.name=${encodeURIComponent(name)}&fields=${FIELDS}&per_page=25`;
  try {
    const res = await fetch(url);
    const j = await res.json();
    const r = bestMatch(j.results ?? [], name);
    if (r) { out.push(mapResult(r)); process.stderr.write(`✓ ${name} → ${r["school.name"]}\n`); }
    else process.stderr.write(`✗ no confident match: ${name}\n`);
  } catch (e) { process.stderr.write(`✗ ${name}: ${e.message}\n`); }
}

const file = `// AUTO-GENERATED by scripts/fetch-scorecard.mjs — real College Scorecard data,
// cached so the app never depends on the live API for these well-known schools.
// Refresh occasionally: node --env-file=.env.local scripts/fetch-scorecard.mjs
import type { ScorecardSchool } from "./scorecard";

export const SCORECARD_CACHE: ScorecardSchool[] = ${JSON.stringify(out.map((s) => ({ ...s, source: "scorecard" })), null, 2)};
`;
writeFileSync("lib/scorecard.data.ts", file);
process.stderr.write(`\nWrote lib/scorecard.data.ts with ${out.length} schools.\n`);
